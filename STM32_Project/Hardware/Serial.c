void USART3_IRQHandler(void)
{
    static uint8_t Rx3State = 0;     // 定义表示当前状态机状态的静态变量
    static uint8_t pRx3Packet = 0;   // 定义表示当前接收数据位置的静态变量
    if (USART_GetITStatus(USART3, USART_IT_RXNE) == SET)  // 判断是否是USART3的接收事件触发的中断
    {
        uint8_t Rx3Data = USART_ReceiveData(USART3);      // 读取数据寄存器，存放在接收的数据变量
        
        if (Rx3State == 0)
        {
            if (Serial3_RxFlag == 0)    // 如果数据确实是包头，并且上一个数据包已处理完毕
            {
                Rx3State = 1;            // 置下一个状态
                pRx3Packet = 0;          // 数据包的位置归零
                Serial3_RxPacket[pRx3Packet] = Rx3Data;  // 将数据存入数据包数组的指定位置
                pRx3Packet ++;           // 数据包的位置自增
            }
        }
        else if (Rx3State == 1)
        {
            if (Rx3Data == '\r')         // 如果收到第一个包尾
            {
                Rx3State = 2;            // 置下一个状态
            }
            else                        // 接收到了正常的数据
            {
                Serial3_RxPacket[pRx3Packet] = Rx3Data;  // 将数据存入数据包数组的指定位置
                pRx3Packet ++;           // 数据包的位置自增
            }
        }
        else if (Rx3State == 2)
        {
            if (Rx3Data == '\n')         // 如果收到第二个包尾
            {
                Rx3State = 0;            // 状态归0
                Serial3_RxPacket[pRx3Packet] = '\0';     // 将收到的字符数据包添加一个字符串结束标志
                Serial3_RxFlag = 1;      // 接收数据包标志位置1，成功接收一个数据包
            }
        }
        
        USART_ClearITPendingBit(USART3, USART_IT_RXNE);  // 清除标志位
    }
}





